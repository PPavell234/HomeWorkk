package Exercises3;

public class HomeWork5 {
    public static void main(String[] args) {
        // Переменные типа float
        float floatVar1 = 3.14f;
        float floatVar2 = 2.718f;

        // Переменные типа double
        double doubleVar1 = 123.456;
        double doubleVar2 = 789.012;

        // Переменные типа boolean
        boolean booleanVar1 = true;
        boolean booleanVar2 = false;

        // Вывод переменных на экран
        System.out.println("Переменные типа float:");
        System.out.println("floatVar1 = " + floatVar1);
        System.out.println("floatVar2 = " + floatVar2);

        System.out.println("\nПеременные типа double:");
        System.out.println("doubleVar1 = " + doubleVar1);
        System.out.println("doubleVar2 = " + doubleVar2);

        System.out.println("\nПеременные типа boolean:");
        System.out.println("booleanVar1 = " + booleanVar1);
        System.out.println("booleanVar2 = " + booleanVar2);
    }
}
