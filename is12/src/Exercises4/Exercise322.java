package Exercises4;

public class Exercise322 {
    //Задание 3.2.2
    public static void main(String[] args) {
        int x = 1;

        boolean number1 = x == 1;   // Истина (true)
        boolean number2 = x != 1;   // Ложь (false)
        boolean number3 = x > 1;    // Ложь (false)
        boolean number4 = x < 1;    // Ложь (false)
        boolean number5 = x >= 1;   // Истина (true)
        boolean number6 = x <= 1;   // Истина (true)

        System.out.println(number1);
        System.out.println(number2);
        System.out.println(number3);
        System.out.println(number4);
        System.out.println(number5);
        System.out.println(number6);
    }
}
